"""
Модели Django для приложения greeting_app.

Модели представляют структуру таблиц базы данных.
Каждый класс модели соответствует одной таблице в БД.
"""

from django.db import models


class UserName(models.Model):
    """
    Модель для хранения имен пользователей в базе данных.
    
    Эта модель создает таблицу в БД с одним полем 'name',
    которое хранит имя пользователя.
    
    Django автоматически создаст:
    - id (первичный ключ) - автоматически добавляется Django
    - name - текстовое поле для хранения имени
    """
    
    # Поле для хранения имени пользователя
    # CharField - поле для хранения строк фиксированной/максимальной длины
    # max_length=100 - максимальная длина имени (100 символов)
    # verbose_name - человекочитаемое имя поля (используется в админке)
    name = models.CharField(
        max_length=100,
        verbose_name='Имя пользователя'
    )
    
    # Поле для автоматического сохранения времени создания записи
    # auto_now_add=True - автоматически устанавливает текущее время при создании
    # Это позволяет отслеживать, когда было добавлено имя
    created_at = models.DateTimeField(
        auto_now_add=True,
        verbose_name='Дата создания'
    )
    
    class Meta:
        """
        Метаданные модели.
        Определяют дополнительные настройки для модели.
        """
        # Человекочитаемое имя модели в единственном числе
        verbose_name = 'Имя пользователя'
        # Человекочитаемое имя модели во множественном числе
        verbose_name_plural = 'Имена пользователей'
        # Сортировка записей по дате создания (от новых к старым)
        ordering = ['-created_at']
    
    def __str__(self):
        """
        Метод для строкового представления объекта.
        Возвращает имя пользователя при выводе объекта.
        Например, в административной панели Django.
        """
        return self.name

